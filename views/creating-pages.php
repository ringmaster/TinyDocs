<h1>Creating Pages</h1>

<p>To create a page in TinyDocs, first be sure that you have logged in as an editor.

<p>In the box at the top of the page, click the "Create New Page" button.  A new editor window will appear with a place to type a page title.  Provide a page title and content, then click "Save" at the bottom of the page to commit your new page to the outline.

<h2 id="enabling-comments">Enabling Comments</h2>

<p>Depending on your TinyDocs settings, comments may or may not be enabled by default.  You can toggle this setting for individual pages using he controls at the bottom of the page editor.</p>

<h2 id="setting-permissions">Setting Permissions</h2>

<p>You can hide pages from visitors to the site that are not logged in using this setting, found at the bottom of the page editor.</p>