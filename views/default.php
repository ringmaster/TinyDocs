<h1>What is TinyDocs?</h1>

<p>TinyDocs is an open source project that you install on your server to provide curated documentation in an organized format to your users.

<p>TinyDocs has wiki-like features that allow you to:</p>
<ul>
	<li>Add new pages</li>
	<li>Edit existing pages while maintaining a history of changes.</li>
	<li>Reorganize content within the table of contents.</li>
	<li>Automate creation of a search index and glossary of all your pages.</li>
	<li>Solicit public comments from visitors on documentation pages to improve their usefulness.</li>
</ul>

